package exemplo02Aula50;

public class Programa {

	public static void main(String[] args) {
		for (int i = 0; i < 5; i++) {
			System.out.println("Valor de i: " + i);
		}
		
		System.out.println("-------------");
		
		for (int j = 4; j >= 0; j--) {
			System.out.println("Valor de j: " + j);
		}

	}

}
